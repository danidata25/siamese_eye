class SiameseNetwork(nn.Module):
    def __init__(self, input_shape):
        super(SiameseNetwork, self).__init__()
        self.cnn = nn.Sequential(
            nn.Conv2d(input_shape[0], 64, kernel_size=10),
            nn.BatchNorm2d(64),
            nn.ReLU(inplace=True),
            nn.MaxPool2d(kernel_size=2, stride=2),
            nn.Conv2d(64, 128, kernel_size=7),
            nn.BatchNorm2d(128),
            nn.ReLU(inplace=True),
            nn.MaxPool2d(kernel_size=2, stride=2),
            nn.Conv2d(128, 128, kernel_size=4),
            nn.BatchNorm2d(128),
            nn.ReLU(inplace=True),
            nn.MaxPool2d(kernel_size=2, stride=2),
            nn.Conv2d(128, 256, kernel_size=4),
            nn.BatchNorm2d(256),
            nn.ReLU(inplace=True)
        )

        # initialize the weights according to the specifications in the paper - later in the class we define _initialize_weights function
        self._initialize_weights()

        # determine the size of the output feature tensor after passing through the CNN with the help of  _get_conv_output that we define later in the class
        n_size = self._get_conv_output(input_shape)

        # Fully connected layer to produce embeddings
        self.fc = nn.Sequential(
            nn.Linear(n_size, 4096),
            nn.BatchNorm1d(4096)
        )

         # Learnable parameters for weighting the component-wise distance
        self.alpha =nn.Sequential(
            nn.Linear(4096,1)
        )


    def _initialize_weights(self):
        for m in self.modules():
            if isinstance(m, nn.Conv2d):
                nn.init.normal_(m.weight, mean=0, std=1e-2)
                nn.init.normal_(m.bias, mean=0.5, std=1e-2)
            elif isinstance(m, nn.Linear):
                nn.init.normal_(m.weight, mean=0, std=2e-1)
                nn.init.normal_(m.bias, mean=0.5, std=1e-2)

    def _get_conv_output(self, shape):
        bs = 1
        input = torch.autograd.Variable(torch.rand(bs, *shape))
        output_feat = self.cnn(input)
        n_size = output_feat.data.view(bs, -1).size(1)
        return n_size

    def forward_one(self, x):
        x = self.cnn(x)
        x = torch.flatten(x, start_dim=1)
        x = self.fc(x)
        return x

    def forward(self, input_a, input_b):
        output_a = self.forward_one(input_a)
        output_b = self.forward_one(input_b)
        abs_diff = torch.abs(output_a - output_b)
        similarity_score = self.alpha(abs_diff)

        return similarity_score

class SiameseLightning(pl.LightningModule):

    def __init__(self, input_shape, learning_rate=1e-3):
        '''
        This method initializes the SiameseLightning model with a specified input shape and learning rate.
        input:
            input_shape (tuple): The shape of the input images.
            learning_rate (float): The learning rate for training the model by defualt-->1e-3.
        This method sets up the Siamese Network model, specifies the loss function as Binary Cross-Entropy with Logits (which does not assume a sigmoid activation in the final layer), and sets the learning rate for the optimizer.
        '''

        super(SiameseLightning, self).__init__()
        self.model = SiameseNetwork(input_shape)
        self.loss_fn = nn.BCEWithLogitsLoss() #assumes the model's final layer doesn't include a sigmoid activation
        self.learning_rate = learning_rate


    def forward(self, input_a, input_b):
        '''
        This method returns the output of the SiameseNetwork
        '''
        return self.model(input_a, input_b)


    def calculate_accuracy(self, similarity_scores, labels, threshold=0.5):
      '''
      This method calculates accuracy after each training and validation step
      to monitor how well the model is learning
      '''
      # apply sigmoid to convert similarity scores to probabilities
      probabilities = torch.sigmoid(similarity_scores)

      # convert probabilities to binary predictions based on the threshold
      predictions = (probabilities >= threshold).float()

      # calculate the number of correct predictions
      correct_predictions = (predictions == labels.float()).float().sum()

      # calculate the accuracy
      accuracy = correct_predictions / labels.size(0)

      return accuracy


    def training_step(self, batch, batch_idx):
        input_a, input_b, labels = batch
        labels=labels.long()
        labels = labels.float()
        similarity_scores = self(input_a, input_b).squeeze()
        loss = self.loss_fn(similarity_scores, labels)
        accuracy = self.calculate_accuracy(similarity_scores, labels)
        self.log('train_loss', loss, on_epoch=True, prog_bar=True)
        self.log('train_accuracy', accuracy, on_epoch=True, prog_bar=True)
        print('train_loss', loss,'train_accuracy', accuracy)
        return loss

    def validation_step(self, batch, batch_idx):
        input_a, input_b, labels = batch
        labels=labels.long()
        labels = labels.float()  # Ensure labels are of type float for BCEWithLogitsLoss
        similarity_scores = self(input_a, input_b).squeeze()  # Ensure similarity scores are squeezed to 1D tensor if necessary
        val_loss = self.loss_fn(similarity_scores, labels)
        val_accuracy = self.calculate_accuracy(similarity_scores, labels)
        self.log('val_loss', val_loss, on_epoch=True, prog_bar=True)
        self.log('val_accuracy', val_accuracy, on_epoch=True, prog_bar=True)
        print('val_loss', val_loss,'val_accuracy', val_accuracy)
        return val_loss

    def configure_optimizers(self):
        optimizer = torch.optim.Adam(self.model.parameters(), lr=self.learning_rate)
        scheduler = torch.optim.lr_scheduler.ExponentialLR(optimizer, gamma=0.99)
        return {'optimizer': optimizer, 'lr_scheduler': scheduler}


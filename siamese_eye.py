class ImagePairDataset(Dataset):
    def __init__(self, data_directory, pairs_file, transform=None):
        self.data_directory = data_directory
        self.pairs_file = pairs_file
        self.image_pairs, self.labels = self.extract_image_pairs()
        self.transform = transform

    def extract_image_pairs(self):
        image_pairs = []
        labels = []  # To store the labels
        with open(self.pairs_file, 'r') as file:
            for line in file:
                pair_info = line.strip().split('\t')
                if len(pair_info) == 3:
                    name, num1, num2 = pair_info
                    label = 1
                    image1 = self.load_image(name, num1)
                    image2 = self.load_image(name, num2)
                    image_pairs.append((image1, image2))
                    labels.append(label)
                elif len(pair_info) == 4:
                    name1, num1, name2, num2 = pair_info
                    label = 0
                    image1 = self.load_image(name1, num1)
                    image2 = self.load_image(name2, num2)
                    image_pairs.append((image1, image2))
                    labels.append(label)
        return image_pairs, labels

    def __len__(self):
        return len(self.image_pairs)

    def __getitem__(self, idx):
        image1, image2 = self.image_pairs[idx]
        label = self.labels[idx]

        # Apply transformations if provided
        if self.transform:
            image1 = self.transform(image1)
            image2 = self.transform(image2)

        return image1, image2, label

    def load_image(self, name, num):
        image_path = os.path.join(self.data_directory, name, f"{name}_{num.zfill(4)}.jpg")
        image = Image.open(image_path)
        return image

